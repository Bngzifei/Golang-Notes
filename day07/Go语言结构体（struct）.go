// Go语言结构体

// Go语言通过自定义的方式形成新的类型,结构体是类型中带有成员的复合类型.Go语言使用结构体和结构体成员来描述真实世界的实体和实体对应的各种属性.

// Go语言中的类型可以被实例化,使用new或者&构造的类型实例的类型是类型的指针.

// 结构体成员是由一系列的成员变量构成,这些成员变量也被称为"字段".字段有以下特性:
// 1.字段用于自己的类型和值
// 2.字段名必须唯一
// 3.字段的类型也可以是结构体,甚至是字段所在结构体的类型.(就是这个字段的类型就是其所属结构体自己)

// 关于Go语言的类(class)
// Go语言中没有"类"的概念,也不支持类的继承等面向对象的概念.

// Go语言的结构体与"类"都是符合结构体,但Go语言中的结构体的内嵌配合接口比面向对象具有更高的扩展性和灵活性.

// Go语言不仅认为结构体能拥有方法,且每种自定义类型也可以拥有自己的方法.

